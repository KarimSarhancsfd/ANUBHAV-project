  currentLang = signal<Language>(this.getInitialLanguage());

  private translationsForCurrentLang = computed(() => this.translations[this.currentLang()]);
  
  constructor() {
    effect(() => {
      const lang = this.currentLang();
      localStorage.setItem('xae_lang', lang);

      // Handle document direction for RTL/LTR languages
      if (typeof document !== 'undefined') {
        document.documentElement.lang = lang;
        if (lang === 'ar') {
          document.documentElement.dir = 'rtl';
        } else {
          document.documentElement.dir = 'ltr';
        }
      }
    });
  }

  private getInitialLanguage(): Language {
    if (typeof localStorage === 'undefined') return 'en';
    const savedLang = localStorage.getItem('xae_lang');
    if (savedLang && ['en', 'es', 'ja', 'ar', 'hi'].includes(savedLang)) {
      return savedLang as Language;
    }
    return 'en';
  }

  translate(key: string): string {
    return this.translationsForCurrentLang()[key] || key;
  }

  setLanguage(lang: Language) {
    this.currentLang.set(lang);
  }
